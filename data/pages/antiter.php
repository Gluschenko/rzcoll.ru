Если Вы едете в общественном транспорте, сообщите об этом водителю. Быть может, Вы спасете жизнь и здоровье многих людей. Ужасно, но есть категория людей, которые сознательно идут на смерть ради совершения акта террора. Они также отличаются от основной массы своим поведением, одеждой, отрешенностью. Одежда должна прикрыть взрывное устройство. Она или явно не по сезону или заметно больше того размера, который смертник носит. Человек знает, что он несет взрывчатку. Он напряжен, опасается прямых контактов с окружающими, сторонится их. Он едет к определенному месту и не заинтересован, чтобы его разоблачили.
 Есть сомнения, запомните приметы, <b><u>позвоните и сообщите</u></b>: в каком направлении он движется, на каком транспорте, как он выглядит. Мы с вами, находясь в стенах правопорядка, должны знать о том, где вероятнее всего можно столкнуться с подготавливаемым террористическим актом. Обнаружить наличие взрывоопасного предмета можно по следующим ПРИЗНАКАМ: – портфели, сумки, пакеты, лежат на полу, в урне, под столом, в оконном проеме. Спросите, где владелец. Если его рядом нет, есть повод для беспокойства;– штатные боеприпасы – гранаты, снаряды, мины, тротиловые шашки. Увидели штатный боеприпас – сразу бейте тревогу;– торчащие из свертка, пакета провода;– звук работающего часового механизма, жужжание либо лежащий в пакете и просматриваемый мобильный телефон или пейджер;– привязанный к пакету натянутый провод или шнур;– неприятный запах либо запах горючего вещества (бензин, керосин и т.п.).<br/><br/>
        
