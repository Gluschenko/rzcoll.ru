<b><u>Памятка по противодействию терроризму</u></b><br/><br/>

         Человечество столкнулось с самым коварным и беспощадным «хищником» – <b><u>террором</u></b>. Для террориста не существует моральных правил. Он фанатик, и его переубедить словами нельзя. Поэтому мы должны смотреть на мир открытыми глазами и не обольщаться тем, что сия горькая чаша нас минует. Противодействие терроризму – задача не только специальных служб. Они будут бессильны, если это противодействие не будет оказываться обществом, каждым гражданином нашей великой страны. Для этого не надо быть суперменом. Обычная житейская смекалка и внимание являются одним из самых эффективных видов противодействия террору. Взрывы домов в ряде городов России показали, что только наша беспечность и безразличие позволили свершиться этим страшным происшествиям. Ведь на глазах жильцов в подвалы завозились мешки с компонентами взрывчатых веществ под видом сахара и других продуктов. Проще простого, увидев такое действие, <b><u>позвонить по телефону</u></b> и попросить проверить. Вам будут благодарны сотрудники специальных служб. Легче проверить, чем потом разбирать завалы и видеть горе людей. Мы знаем о многочисленных случаях террористических актов, совершенных с использованием автомобилей, начиненных взрывчаткой. Конечно, определить на улице такой автомобиль простому человеку невозможно. Но , увидев у своего дома припаркованную чужую машину, можно и нужно обеспокоиться, <b><u>позвонить по телефону</u></b>  и попросите проверить. Пусть Вас не гложет мысль о том, что Вы причинили неудобства спецслужбам, пусть Вас не беспокоит боязнь того, что Вас назовут паникером. Вы платите налоги, на которые содержатся специальные службы, обеспечивающие Вашу безопасность. Излюбленный метод террористов – использовать сумку, портфель, пакет, сверток, начиненный взрывчаткой, и положить его в мусорный контейнер или урну, оставить у прилавка, под столом, в салоне общественного транспорта, кинотеатре, спортивном комплексе. Но ведь все мы взрослые люди и знаем, что просто так пакет или сверток в мус